package chapter_III;

public class Rectangle3_23 {

    private double posX;
    private double posY;
    private double width;
    private double height;

    public Rectangle3_23(double x, double y, double width, double height){
        this.posX = x;
        this.posY = y;
        this.width = width;
        this.height = height;
    }

    public double getPosX() {
        return posX;
    }

    public void setPosX(double posX) {
        this.posX = posX;
    }

    public double getPosY() {
        return posY;
    }

    public void setPosY(double posY) {
        this.posY = posY;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
