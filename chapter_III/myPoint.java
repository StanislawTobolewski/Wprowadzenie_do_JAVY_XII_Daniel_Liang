package chapter_III;

public class myPoint {
    private double X;
    private double Y;

    public myPoint(double x, double y){
        this.X = x;
        this.Y = y;
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }
}
