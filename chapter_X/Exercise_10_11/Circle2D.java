package chapter_X.Exercise_10_11;

public class Circle2D {

    private double x;
    private double y;

    public Circle2D(){}

    public Circle2D(int x, int y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
